package twitterPack;

public class Segue {
	
	private int id;
	private int id_segue;
	private int id_seguito;
	
	public Segue(){
		
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getId_segue() {
		return id_segue;
	}
	public void setId_segue(int id_segue) {
		this.id_segue = id_segue;
	}
	public int getId_seguito() {
		return id_seguito;
	}
	public void setId_seguito(int id_seguito) {
		this.id_seguito = id_seguito;
	}
	
	

}
